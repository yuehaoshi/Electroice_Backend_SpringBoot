package com.yuehao.ecommerce.dao;

import com.yuehao.ecommerce.entity.Product;
import org.springframework.data.domain.Page;
import org.springframework.data.domain.Pageable;
import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.web.bind.annotation.CrossOrigin;
import org.springframework.web.bind.annotation.RequestParam;

@CrossOrigin("http://localhost:4200") //Accept calls from web browser scripts for this origin(protocol+hostname+port)
public interface ProductRepository extends JpaRepository<Product, Long> { //Entity is Product, Primary key is Long
    Page<Product> findByCategoryId(@RequestParam("id") Long id, Pageable pageable);

    Page<Product> findByNameContaining(@RequestParam("name") String name, Pageable pageable);
}
